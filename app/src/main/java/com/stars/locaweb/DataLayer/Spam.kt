@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun SpamScreen(navController: NavController, emailViewModel: EmailViewModel = viewModel()) {
    val spamEmails by emailViewModel.getSpamEmails().collectAsState(emptyList())

    Scaffold(
        topBar = {
            TopAppBar(
                title = { Text("Spam") },
                navigationIcon = {
                    IconButton(onClick = { navController.popBackStack() }) {
                        Icon(Icons.Default.ArrowBack, contentDescription = "Back", tint = Color.White)
                    }
                },
                colors = TopAppBarDefaults.smallTopAppBarColors(
                    containerColor = MaterialTheme.colorScheme.primary,
                    titleContentColor = Color.White
                )
            )
        }
    ) { paddingValues ->
        LazyColumn(
            contentPadding = paddingValues,
            modifier = Modifier
                .fillMaxSize()
                .padding(16.dp)
        ) {
            items(spamEmails) { email ->
                SpamEmailListItem(email, onClick = {
                })
            }
        }
    }
}

@Composable
fun SpamEmailListItem(email: EmailItem, onClick: () -> Unit) {
    Card(
        modifier = Modifier
            .fillMaxWidth()
            .padding(vertical = 8.dp)
            .clickable(onClick = onClick),
        elevation = CardDefaults.cardElevation(4.dp)
    ) {
        Column(modifier = Modifier.padding(16.dp)) {
            Text(text = email.subject, fontWeight = FontWeight.Bold)
            Spacer(modifier = Modifier.height(4.dp))
            Text(text = email.sender, color = Color.Gray)
            Spacer(modifier = Modifier.height(8.dp))
            Text(text = email.preview, maxLines = 2)
        }
    }
}